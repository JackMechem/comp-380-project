package com.inc.fcr.car.enums;

/**
 * Represents the drivetrain configuration of a vehicle.
 *
 * <ul>
 *   <li>{@link #FWD} - Front-wheel drive</li>
 *   <li>{@link #RWD} - Rear-wheel drive</li>
 *   <li>{@link #AWD} - All-wheel drive</li>
 * </ul>
 */
public enum Drivetrain {
    /** Front-wheel drive. */
    FWD,
    /** Rear-wheel drive. */
    RWD,
    /** All-wheel drive. */
    AWD
}
