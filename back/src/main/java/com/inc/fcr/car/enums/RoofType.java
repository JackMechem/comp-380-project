package com.inc.fcr.car.enums;

/**
 * Represents the roof style of a vehicle.
 *
 * <ul>
 *   <li>{@link #SOFTTOP}   - Fabric convertible top</li>
 *   <li>{@link #HARDTOP}   - Rigid fixed or removable roof</li>
 *   <li>{@link #TARGA}     - Partial open-top with a fixed roll bar</li>
 *   <li>{@link #SLICKTOP}  - Full roof panel removed</li>
 *   <li>{@link #SUNROOF}   - Single sliding glass panel</li>
 *   <li>{@link #PANORAMIC} - Extended glass roof panel</li>
 * </ul>
 */
public enum RoofType {
    SOFTTOP, HARDTOP, TARGA, SLICKTOP, SUNROOF, PANORAMIC
}